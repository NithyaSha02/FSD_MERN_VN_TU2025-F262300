let marks = 85;  
let age = 17;    

// Grade calculation
if (marks >= 90) {
    console.log("Grade: A");
} else if (marks >= 75) {
    console.log("Grade: B");
} else if (marks >= 50) {
    console.log("Grade: C");
} else {
    console.log("Grade: F");
}

// Age check using ternary operator
let status = (age >= 18) ? "Adult" : "Minor";
console.log("Status: " + status);



